const max = s => {
    let charMap = {}
    for (char of s) {
        if (charMap[char]) {
            charMap[char]++
        } else {
            charMap[char] = 1
        }
    }
    let most = 0,
        letter = null;
    for (char in charMap) {
        if (charMap[char] > most) {
            most = charMap[char]
            letter = char
        }
    }
    return letter
}

